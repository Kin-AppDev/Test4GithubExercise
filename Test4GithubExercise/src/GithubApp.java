//Just a program for the Github exercise of git basic commands

public class GithubApp {
	public static void main(String[] args) {

		double a, b, x, y, answer;
		a = 10;
		b = 20;
		x = 50;
		y = 40;
			
	//Add
		answer = x + y;
		System.out.println("Answer for addition = " + answer);
		
	//Subtract
		answer = x - y;
		System.out.println("Answer for subtraction = " + answer);
		
	//Multiply
		answer = a * b;
		System.out.println("Answer for multiplication = " + answer);
		
	//Divide
		answer = x / y;
		System.out.println("Answer for division = " + answer);
			
	}
}
